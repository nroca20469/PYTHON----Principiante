# Type() --> Consultar el tipo de dato
print(type("Hola python"));  # El tipo de 'Hola Python' es <class 'str'> --> String.
print(type(5));  # El tipo de 5 es <class 'int'> --> Int.
print(type(1.5));  # El tipo de 5.5 es <class 'float'> --> Float. -> numerico no entero
print(type(True));   # El tipo de True es <class 'bool'> --> Booleano.
print(type(1 + 1j));	# El tipo de 1 + 1j es <class 'complex'> --> Complejo.	 

